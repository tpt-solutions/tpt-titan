func renderEquationPNG(latex string, scale float64, fg color.Color) ([]byte, error) {
	const cellW, cellH, pad = 6, 7, 6 // glyph advance width includes 1px spacing
	if scale <= 0 {
		scale = 1
	}
	w := int(math.Ceil(float64(len(latex)*cellW+2*pad) * scale))
	h := int(math.Ceil(float64((cellH+2*pad)) * scale))
	if w < 1 {
		w = 1
	}
	if h < 1 {
		h = 1
	}

	img := image.NewRGBA(image.Rect(0, 0, w, h))
	// White background.
	for y := 0; y < h; y++ {
		for x := 0; x < w; x++ {
			img.Set(x, y, color.White)
		}
	}

	cx := int(float64(pad) * scale)
	baseY := int(float64(pad) * scale)
	for _, r := range latex {
		glyph, ok := font5x7[r]
		if !ok {
			glyph = font5x7['?']
		}
		drawGlyph(img, glyph, cx, baseY, int(scale), fg)
		cx += int(float64(cellW) * scale)
	}

	var buf bytes.Buffer
	if err := png.Encode(&buf, img); err != nil {
		return nil, fmt.Errorf("failed to encode PNG: %w", err)
	}
	return buf.Bytes(), nil
}

func drawGlyph(img *image.RGBA, glyph [7]byte, ox, oy, scale int, fg color.Color) {
	for row := 0; row < 7; row++ {
		bits := glyph[row]
		for col := 0; col < 5; col++ {
			if bits&(1<<uint(4-col)) != 0 {
				for dy := 0; dy < scale; dy++ {
					for dx := 0; dx < scale; dx++ {
						x := ox + col*scale + dx
						y := oy + row*scale + dy
						if x < img.Bounds().Dx() && y < img.Bounds().Dy() {
							img.Set(x, y, fg)
						}
					}
				}
			}
		}
	}
}

func parseHexColor(s string) (color.Color, bool) {
	s = strings.TrimSpace(s)
	if strings.HasPrefix(s, "#") {
		s = s[1:]
	}
	if len(s) != 6 {
		return nil, false
	}
	var r, g, b uint8
	if _, err := fmt.Sscanf(s, "%02x%02x%02x", &r, &g, &b); err != nil {
		return nil, false
	}
	return color.RGBA{R: r, G: g, B: b, A: 255}, true
}
