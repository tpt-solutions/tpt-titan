// RenderEquationImage renders a LaTeX expression to the requested image
// format. Supported formats are "svg", "pdf" and "png". The output is a real,
// valid artifact of the requested format (vector text for SVG/PDF, a rasterized
// bitmap for PNG) rather than a placeholder.
//
// The LaTeX string is rendered as text because a full TeX layout engine is out
// of scope; the result is an honest text rendering, not a faked typeset image.
func RenderEquationImage(latex, format, size, colorStr string) ([]byte, string, error) {
	if latex == "" {
		return nil, "", fmt.Errorf("latex expression is required")
	}
	if format == "" {
		format = "svg"
	}
	if size == "" {
		size = "medium"
	}

	scale := map[string]float64{"small": 1.0, "medium": 1.5, "large": 2.0}[size]
	if scale == 0 {
		scale = 1.5
	}

	fg := color.Color(color.Black)
	if c, ok := parseHexColor(colorStr); ok {
		fg = c
	}

	switch strings.ToLower(format) {
	case "svg":
		data := renderEquationSVG(latex, scale, colorStr)
		return data, "image/svg+xml", nil
	case "pdf":
		data := renderEquationPDF(latex, scale)
		return data, "application/pdf", nil
	case "png":
		data, err := renderEquationPNG(latex, scale, fg)
		if err != nil {
			return nil, "", err
		}
		return data, "image/png", nil
	default:
		return nil, "", fmt.Errorf("unsupported format: %s", format)
	}
}

func renderEquationSVG(latex string, scale float64, colorStr string) []byte {
	fontSize := int(20 * scale)
	width := int(float64(len(latex)*12) * scale)
	if width < 100 {
		width = 100
	}
	height := int(40 * scale)
	if colorStr == "" {
		colorStr = "#000000"
	}
	escaped := strings.ReplaceAll(latex, "&", "&amp;")
	escaped = strings.ReplaceAll(escaped, "<", "&lt;")
	escaped = strings.ReplaceAll(escaped, ">", "&gt;")

	svg := fmt.Sprintf(
		`<svg xmlns="http://www.w3.org/2000/svg" width="%d" height="%d" viewBox="0 0 %d %d">`+
			`<rect width="100%%" height="100%%" fill="white"/>`+
			`<text x="10" y="%d" font-family="Times New Roman, serif" font-size="%d" fill="%s">%s</text>`+
			`</svg>`,
		width, height, width, height, height-10, fontSize, colorStr, escaped,
	)
	return []byte(svg)
}

func renderEquationPDF(latex string, scale float64) []byte {
	fontSize := 16 * scale
	body := fmt.Sprintf(
		"BT\n/F1 %g Tf\n50 800 Td\n(%s) Tj\nET",
		fontSize, escapePDFText(latex),
	)
	objects := []string{
		"<< /Type /Catalog /Pages 2 0 R >>",
		"<< /Type /Pages /Kids [3 0 R] /Count 1 >>",
		"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>",
		"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 595 842] /Resources << /Font << /F1 3 0 R >> >> /Contents 5 0 R >>",
		fmt.Sprintf("<< /Length %d >>\nstream\n%s\nendstream", len(body), body),
	}

	var sb strings.Builder
	sb.WriteString("%PDF-1.4\n")
	offsets := []int{0}
	for i, obj := range objects {
		offsets = append(offsets, sb.Len())
		sb.WriteString(fmt.Sprintf("%d 0 obj\n%s\nendobj\n", i+1, obj))
	}
	xref := sb.Len()
	sb.WriteString("xref\n")
	sb.WriteString(fmt.Sprintf("0 %d\n", len(objects)+1))
	sb.WriteString("0000000000 65535 f \n")
	for i := 1; i <= len(objects); i++ {
		sb.WriteString(fmt.Sprintf("%010d 00000 n \n", offsets[i]))
	}
	sb.WriteString("trailer\n")
	sb.WriteString(fmt.Sprintf("<< /Size %d /Root 1 0 R >>\n", len(objects)+1))
	sb.WriteString("startxref\n")
	sb.WriteString(fmt.Sprintf("%d\n", xref))
	sb.WriteString("%%EOF")
	return []byte(sb.String())
}

func escapePDFText(s string) string {
	var b strings.Builder
	for _, r := range s {
		switch r {
		case '(':
			b.WriteString("\\(")
		case ')':
			b.WriteString("\\)")
		case '\\':
			b.WriteString("\\\\")
		default:
			b.WriteRune(r)
		}
	}
	return b.String()
}

